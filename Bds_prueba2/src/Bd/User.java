
package Bd;


public class User {
    private String nombre;
    private String rut;
    private int fono;
    

    public String getNombre() {
        return nombre;
    }

    public String getRut() {
        return rut;
    }

    public int getFono() {
        return fono;
    }

    

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public void setRut(String rut) {
        this.rut = rut;
    }

    public void setFono(int fono) {
        this.fono = fono;
    }

  
    
}
